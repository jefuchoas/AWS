CREATE TABLE dados (
    id  INTEGER PRIMARY KEY autoincrement,
	anemometer varchar(15),
    temperature varchar(15),
    humidity varchar(15),
    windvane varchar(15),
	data varchar(15),
    hora varchar(15),
    moisture varchar(15),
    intensity varchar(15),
    tempMinima varchar(15),
    tempMaxima varchar(15),
    chAcumulada varchar(15),
    chuvaTresDias varchar(15)
);